export function getRugBySlug(slug: string): Rug | undefined {
  return rugs.find(r => r.slug === slug)
}

export function getRelatedRugs(rug: Rug): { rug: Rug; label: string }[] {
  return rug.related_rug_slugs
    .map((slug, i) => {
      const found = rugs.find(r => r.slug === slug)
      if (!found) return null
      return { rug: found, label: rug.related_rug_labels[i] || '' }
    })
    .filter(Boolean) as { rug: Rug; label: string }[]
}
